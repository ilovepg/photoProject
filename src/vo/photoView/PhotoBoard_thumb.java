package vo.photoView;

public class PhotoBoard_thumb {
	int photo_thumbNo; //썸네일 이미지 고유번호
	String photo_thumbType; //썸네일 이미지 타입(sub,main)
	String photo_thumbName; //썸네일 이미지명
	
	public int getPhoto_thumbNo() {
		return photo_thumbNo;
	}
	public void setPhoto_thumbNo(int photo_thumbNo) {
		this.photo_thumbNo = photo_thumbNo;
	}
	public String getPhoto_thumbType() {
		return photo_thumbType;
	}
	public void setPhoto_thumbType(String photo_thumbType) {
		this.photo_thumbType = photo_thumbType;
	}
	public String getPhoto_thumbName() {
		return photo_thumbName;
	}
	public void setPhoto_thumbName(String photo_thumbName) {
		this.photo_thumbName = photo_thumbName;
	}
	
	
	
}
